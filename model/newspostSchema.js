import mongoose from "mongoose";

const Schema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const newspost = mongoose.model("newspost", Schema);

export default newspost;